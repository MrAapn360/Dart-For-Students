// Using print() Function

/*
  print() displays output on the screen.
  It automatically moves to the next line after printing.
*/

void main() {

  // Printing a String
  print("Hello, World!");

  // Printing a number
  print(100);

  // Printing a decimal
  print(3.14);

  // Printing a boolean
  print(true);

  // Printing a variable
  String name = "Ali";
  int age = 20;
  double gpa = 3.8;

  print(name);
  print(age);
  print(gpa);

  // Printing a simple expression
  print(10 + 5);   // 15
  print(10 > 5);   // true

}

/*
  OUTPUT:
  Hello, World!
  100
  3.14
  true
  Ali
  20
  3.8
  15
  true
*/
